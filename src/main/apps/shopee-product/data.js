function norm(rawdata) {
  const ret = [];
  for (let i = 0; i < rawdata.price.length; i++) {
    const date = new Date(rawdata.price_ts[i]);
    const discount = randInt(20, 40);
    ret.push({
      key: `index-${i}`,
      ts: rawdata.price_ts[i],
      price: rawdata.price[i],
      discount,
      priceBeforeDiscount: ((discount + 100) * rawdata.price[i]) / 100,
      date: `${date.getDate()}/${date.getMonth() + 1}/${date.getFullYear()}`,
    });
  }
  return ret;
}

export const data = norm(rawdata);
